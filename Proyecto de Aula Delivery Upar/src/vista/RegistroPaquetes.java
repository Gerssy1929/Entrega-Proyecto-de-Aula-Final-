    private void cmbtypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbtypeItemStateChanged
        String largo="", ancho="", alto="", descri="";
        if (cmbtype.getSelectedIndex()>0){
            switch (cmbtype.getSelectedIndex()){
                case 1:largo="0"; ancho="0"; alto="0"; descri="Ninguna"; txtpeso.requestFocus(); break;
                case 2:descri="Ninguna"; txtlargo.requestFocus(); break;
                case 3:largo="0"; ancho="0"; alto="0";txtdescri.requestFocus(); break;
            }
            txtlargo.setText(largo);
            txtancho.setText(ancho);
            txtalto.setText(alto);
            txtdescri.setText(descri);
            
        }
    }//GEN-LAST:event_cmbtypeItemStateChanged

