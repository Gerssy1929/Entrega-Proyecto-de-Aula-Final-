 public Paquete leerDatosComponentes(){
        
        Paquete p = new Paquete();
        p.setNum(Long.valueOf(this.ftnum.getValue().toString()));
        p.setTipoPaq(this.cmbtype.getSelectedItem().toString());
        p.setLargo(Double.valueOf(this.txtlargo.getText()));
        p.setAncho(Double.valueOf(this.txtancho.getText()));
        p.setAlto(Double.valueOf(this.txtalto.getText()));
        p.setDescri(this.txtdescri.getText());
        p.setPeso(Double.valueOf(this.txtpeso.getText()));
        p.setPrecio(Long.valueOf(this.txtprecio.getText()));
        p.setId(this.txtID.getText());
       
        return p;
    
    }
    
    
    public void guardar(){
        Paquete p = this.leerDatosComponentes();
        try {
            this.logica.escribir(p);
            VentanaEmergente.mostrarMsg(this, "Confirmacion", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (IOException | NullPointerException  ex) {
            
            VentanaEmergente.mostrarMsg(this, "Error", ex.getMessage(), JOptionPane.ERROR_MESSAGE);
        }
       
       
    }
